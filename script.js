const languageButtons = document.querySelectorAll("[data-language-button]");

let currentLanguage = "en";

function getTranslation(key) {
  return translations[currentLanguage][key] || translations.en[key] || "";
}

function updateMetaTag(selector, value) {
  const tag = document.querySelector(selector);

  if (tag) {
    tag.setAttribute("content", value);
  }
}

function applyLanguage(language) {
  currentLanguage = language;
  document.documentElement.lang = language;
  document.documentElement.dataset.language = language;
  document.title = getTranslation("metaTitle");

  updateMetaTag('meta[name="description"]', getTranslation("metaDescription"));
  updateMetaTag('meta[property="og:title"]', getTranslation("ogTitle"));
  updateMetaTag('meta[property="og:description"]', getTranslation("ogDescription"));

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.innerHTML = getTranslation(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    element.setAttribute("aria-label", getTranslation(element.dataset.i18nAriaLabel));
  });

  languageButtons.forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.languageButton === language));
  });
}

document.querySelectorAll('a[href^="#"]').forEach((link) => {
  link.addEventListener("click", (event) => {
    const target = document.querySelector(link.getAttribute("href"));

    if (!target) {
      return;
    }

    event.preventDefault();
    target.scrollIntoView({ behavior: "smooth", block: "start" });
  });
});

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.languageButton);
  });
});

applyLanguage(currentLanguage);
